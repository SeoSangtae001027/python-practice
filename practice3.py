print(6/3) # 2
print(2**3) # 2 ^ 3
print(5%3) # 5를 3으로 나눈 나머지 2
print(7//3) # 7을 3으로 나눈 몫 2
print(3+4 == 7) # ==의 앞과 뒤가 똑같나는 의미 True
print(1 != 3) # !=의 앞과 뒤가 같지 않는다는 의미 True
print((3 > 0) and (3 < 5)) # and 조건은 앞과 뒤가 모두 만족해야함.  True
# and 대신에 & 를 사용 가능함.
print((3 > 0) or (3 > 5)) # or 조건은 앞과 뒤 둘중 하나만 만족해도됨. True
# or 대신에 | 를 사용 가능함.
number = 2 + 3 * 4 #14
print(number)
number = number + 2 #16 
# number += 2 라는 문장이 위의 식과 같은 의미임.
# 곱셈뿐만이 아닌 다른 연산식도 응용 가능함.
print(number)